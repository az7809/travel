# ---------------------------------------------------------------------------
# 公开 API
# ---------------------------------------------------------------------------
def get_system_prompt(security_mode: str = "standard") -> str:
    """返回对应安全级别的 System Prompt。

    Args:
        security_mode: "standard" (标准安全) 或 "concierge" (时装周特护模式)
    """
    prompt = SYSTEM_PROMPT_BASE
    if security_mode == "concierge":
        prompt += CONCIERGE_SECURITY_INJECTION
    return prompt


